/// Input -> Operation -> Output
double add(double num1, double num2) {
  // int result = num1 + num2;
  return num1 + num2;
}

// Subtratct Function
double subtract(double num1, double num2) {
  // int result = num1 - num2;
  return num1 - num2;
}

void main() {
  print(add(12,12));
  print(add(32, 78));
  print(subtract(12, 12));
}
